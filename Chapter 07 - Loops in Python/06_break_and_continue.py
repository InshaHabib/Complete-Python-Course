# Break and continue

for i in range(100):
    if(i==34):
        break # exit the loop right now
    print(i)
    
    
for i in range(100):
    if(i==34):
        continue # skip the iteration right now
    print(i)
    
for i in range(0,80):
    print(i)
    if i==3:
        break

for i in range(4):
    print("INSHA HABIB")
    if i==2:
        continue  # skip the iteration when i is 2  
    print(i)
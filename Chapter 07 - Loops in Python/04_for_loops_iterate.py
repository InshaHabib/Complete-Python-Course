# For loops in Python
# Print the content of a list using for loops
list = [1,4,67,89,234]
for i in list:
    print(i)
    
# Print the content of a tuple using for loops
# Tuples are immutable sequences in Python
t = (1,34,90,122,567)
for i in t:
    print(i)
        
# Print the content of a string using for loops
s = "Insha_Habib"
for i in s:
    print(i)
    
